def connect():
    conn = sqlite3.connect("books.db")
    cur = conn.cursor()
    cur.execute("CREATE TABLE IF NOT EXISTS book (id INTEGER PRIMARY KEY, title TEXT, author TEXT, year INTEGER CHECK(year BETWEEN 1900 AND 2030), isbn INTEGER CHECK(isbn BETWEEN 1000000000 AND 9999999999999))")
    os.system('attrib +h books.db')
    conn.commit()
    conn.close()

